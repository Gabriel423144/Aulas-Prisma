export function calculaAreaRetangulo(altura, largura){
    return altura * largura;
}

export function calculaPerimetroRetangulo(altura, largura){
    return (altura + largura) * 2;
}

export function calculaAreaCirculo(raio){
    return Math.PI *(raio ** 2);
}

export function calculaCircunferencia(raio){
   return 2 * Math.PI * raio;
}